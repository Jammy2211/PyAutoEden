class AutoLabels:
    def __init__(
        self,
        title=None,
        ylabel=None,
        xlabel=None,
        yunit=None,
        cb_unit=None,
        legend=None,
        filename=None,
    ):
        self.title = title
        self.ylabel = ylabel
        self.xlabel = xlabel
        self.yunit = yunit
        self.cb_unit = cb_unit
        self.legend = legend
        self.filename = filename
